import random

# Задание №6
number_1 = float(input("Введите первое число (используйте точку для дробных чисел): "))
number_2 = float(input("Введите второе число (используйте точку для дробных чисел): "))
result = None
while True:
    math_or_exit_operation = input("Введите необходимую операцию:\n+ (add); - (sub); * (multi); / (div); 0 (exit): ")
    if math_or_exit_operation == "0":  # Если введен "0", то выходим
        break
    elif math_or_exit_operation == "add":  # Если введен математический оператор сложения, то складываем числа
        result = number_1 + number_2
    elif math_or_exit_operation == "sub":  # Если введен математический оператор вычитания, то из 1-го вычитаем 2-ое
        result = number_1 - number_2
    elif math_or_exit_operation == "multi":  # Если введен математический оператор умножения, то перемножаем числа
        result = number_1 * number_2
    elif math_or_exit_operation == "div":  # Введен математический оператор деления
        if number_2 != 0:  # Если 2-ое число не равно 0, то 1-ое число делим на 2-ое
            result = number_1 / number_2
        else:  # Если 2-ое число равно 0, то выводим сообщение "Деление на ноль"
            print("Деление на ноль")
    print(result)  # Выводим результат арифметического действия

