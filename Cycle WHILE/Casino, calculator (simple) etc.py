"""
Компьютер генерирует число от 1 до 10 и цвет (красный или черный), 
Пользователю дается 5 попыток угадать номер и цвет.
(Пример введения с клавиатуры: 3 красный)
В случае неудачи вывести на экран правильную комбинацию
"""
list_color = ["красный", "черный"]  # Создаем список для хранения элементов: красный и черный
number_computer = random.randint(1, 10)  # Генерируем случайной число
color_computer = random.choice(list_color)  # Генерируем случайный цвет из списка
number_user = None
color_user = None
attempt_user = 0  # создаем счетчик для попыток пользователя
while attempt_user < 5:  # Итераций будет 5 (попыток пользователя)
    number_user = int(input("Введите число (от 1 до 10): "))
    color_user = input("Введите цвет (красный / черный): ")
    # Проверяем ввел ли пользователь число и цвет в разрешенном диапазоне
    attempt_user += 1  # Прибавление в счетчик кол-во попыток
    if 0 < number_user <= 10 and (color_user == "красный" or color_user == "черный"):
        # Если число и цвет введенные пользователем совпадает со случайно генерируемым числом, то выводим "Вы угадали!"
        if number_user == number_computer and color_user == color_computer:
            print("Вы угадали!")  # и цикл останавливается
            break
        elif attempt_user == 5:
            # Если после 5 попыток пользователь не угадал, то выводим загаданные число и цвет
            print(
                f"К сожалению вы не угадали. Правильная комбинация:\nЦифра: {number_computer}\nЦвет: {color_computer}")
    else:
        # Если пользователь ввел некорректные данные, то выводим сообщение
        print("Вы ввели некорректные данные! Посмотрите внимательно разрешенные диапазоны значений.")
